public class ValidPerfectSquare {
    /**
     * 1+3+5+...+(2n-1)=n*n
     *
     * @param num
     * @return
     */
    public boolean isPerfectSquare(int num) {
        int index = 1;
        while (num > 0) {
            num -= index;
            index += 2;
        }
        return num == 0;
    }

    /**
     * 二分查找
     *
     * @param num
     * @return
     */
    public boolean isPerfectSquare2(int num) {
        if (num < 2) {
            return true;
        }

        long left = 2, right = num / 2, x, guessSquared;
        while (left <= right) {
            x = left + (right - left) / 2;
            guessSquared = x * x;
            if (guessSquared == num) {
                return true;
            }
            if (guessSquared > num) {
                right = x - 1;
            } else {
                left = x + 1;
            }
        }
        return false;
    }
}
